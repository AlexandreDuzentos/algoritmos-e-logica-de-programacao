package curso_programacao;

public class Main {
    
	/*
	 * O java é uma linguagem orientada a objetos, por essa razão, todo
	 * código que escrevemos deve estar dentro de uma classe.
	 * 
	 * Esse é o método(Main) que permite que a classe seja seja um programa
	 * executável.
	 * 
	 * Um programa java executável, deve ter pelo menos uma declaração de
	 * funçao, igual ao método abaixo.
	 * 
	 * A declaração abaixo define o entrypoint da aplicação, ou seja é o
	 * ponto de entrada, é onde a execução começará.  
	*/
	public static void main(String[] args) {
		
		System.out.println("Olá mundo!");
    
	}

}
